// Only the top N players per team make the draftable pool (rosters are listed
// best-to-worst).
const TOP_PER_TEAM = 10;

// Apply the rating curve (height/weight/wingspan stay as real measurements).
export function applyCurve<T extends Player>(pl: T): T {
  return {
    ...pl,
    athleticism: spread(pl.athleticism),
    speed: spread(pl.speed),
    ballHandling: spread(pl.ballHandling),
    shooting: spread(pl.shooting),
    postScoring: spread(pl.postScoring),
    defense: spread(pl.defense),
    passing: spread(pl.passing),
    iq: spread(pl.iq),
    attitude: spread(pl.attitude),
    clutch: spread(pl.clutch),
    durability: spread(pl.durability),
  };
}

// Flatten current rosters (top N per team), attaching team + a stable id.
const CURRENT_PLAYERS: Player[] = Object.entries(ROSTERS).flatMap(
  ([team, roster]) => roster.slice(0, TOP_PER_TEAM).map(pl =>
    applyCurve({ ...pl, id: slug(pl.name), team })),
);

const currentIds = new Set(CURRENT_PLAYERS.map(p => p.id));

// Full draft pool: current players + historic legends (legends that duplicate a
// current player's id are dropped, so nobody on a current roster appears twice).
const POOL: Player[] = [
  ...CURRENT_PLAYERS,
  ...LEGENDS.filter(l => !currentIds.has(l.id)),
  ...WNBA_PLAYERS.filter(w => !currentIds.has(w.id)),
  ...CHARACTERS,
];

// Keep real players only if we have a headshot for them; celebrity/fictional
// characters ('STAR') are always kept (they use a wildcard initials avatar).
export const PLAYERS: Player[] = POOL.filter(
  p => p.team === 'STAR' || playerImageUrl(p.id) !== null,
);

export const PLAYERS_BY_ID: Record<string, Player> =
  Object.fromEntries(PLAYERS.map(pl => [pl.id, pl]));

export const PLAYERS_BY_TEAM: Record<string, Player[]> = PLAYERS.reduce(
  (acc, pl) => { (acc[pl.team] ||= []).push(pl); return acc; },
  {} as Record<string, Player[]>,
);
